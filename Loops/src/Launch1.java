
public class Launch1 {

	public static void main(String[] args) {
		//FOR-LOOP
		for(int i=0; i<5; i++) {
			System.out.print("*");
		}
		System.out.println();
		
		//WHILE-LOOP
		int a = 5;
		int j =0;
		while(j<a) {
			System.out.println("Tanishq");
			j++;
		}
		
		//DO-WHILE LOOP
		
		int b = 5;
		int k = 0;
		do {
			System.out.println("Sharma");
			k++;
		}while(k<b);

	}

}
